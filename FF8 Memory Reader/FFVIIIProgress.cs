        public string GetStoryPoint(int id)
        {
            if (storyProgression.ContainsKey(id))
            {
                var locations = new FFVIIILocation();
                return locations.locationList[storyProgression[id]];
            }
            return "???";
        }
        public string GetProgress(int id)
        {
            if (storyProgression.ContainsKey(id))
            {
                //find index of dictionary elements by key.
                int index = storyProgression.Keys.ToList().IndexOf(id);

                int totalStory = storyProgression.Count;

                double progress = (index + 1) / totalStory;
                double percentProgress = progress * 100;
                double roundedPercentProgress = Math.Round(percentProgress, 2);

                return roundedPercentProgress.ToString() + "%";
            }
            return "???";
        }

